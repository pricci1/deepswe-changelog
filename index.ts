const BASE_URL = "https://deepswe.datacurve.ai/artifacts";

const ARTIFACTS = [
  "summary",
  "leaderboard",
  "comparison",
  "analysis",
  "critiques",
  "corpus-stats",
  "repositories",
  "verification-behavior",
  "tasks",
] as const;

type Artifact = (typeof ARTIFACTS)[number];
type JsonValue = null | boolean | number | string | JsonValue[] | { [key: string]: JsonValue };
type JsonObject = { [key: string]: JsonValue };

const RAW_DIR = "data/raw";
const REPORT_PATH = "data/README.md";
const MAIN_README_PATH = "README.md";
const OMIT_MERMAID = process.env.OMIT_MERMAID === "1";
const IGNORED_TOP_LEVEL_KEYS = new Set([
  "scope",
  "unit",
  "description",
  "generated_at",
  "selection_source",
  "deep_swe_source",
  "swebenchpro_source",
]);

async function main() {
  await Bun.$`mkdir -p ${RAW_DIR}`;

  const artifacts = new Map<string, JsonObject>();

  for (const artifact of ARTIFACTS) {
    const url = `${BASE_URL}/${artifact}.json`;
    const response = await fetch(url, {
      headers: { accept: "application/json" },
    });

    if (!response.ok) {
      throw new Error(`Failed to fetch ${url}: ${response.status} ${response.statusText}`);
    }

    const data = normalizeArtifact(artifact, (await response.json()) as JsonObject);
    artifacts.set(artifact, data);
    await Bun.write(`${RAW_DIR}/${artifact}.json`, `${stableStringify(data)}\n`);
  }

  await Bun.write(REPORT_PATH, renderReport(artifacts));
  await Bun.write(MAIN_README_PATH, renderMainReadme(artifacts));
}

function stableStringify(value: JsonValue): string {
  return JSON.stringify(sortJson(value), null, 2);
}

function normalizeArtifact(artifact: Artifact, data: JsonObject): JsonObject {
  const normalized = Object.fromEntries(
    Object.entries(data).filter(([key]) => !IGNORED_TOP_LEVEL_KEYS.has(key)),
  ) as JsonObject;

  if (artifact === "comparison") {
    normalized.rows = getRows(normalized).map((row) => omitKeys(row, ["swebenchpro_source_url"]));
  }

  if (artifact === "leaderboard") {
    normalized.rows = getRows(normalized).map((row) => omitKeys(row, ["ci_method", "selection_notes"]));
  }

  if (artifact === "repositories") {
    normalized.repositories = getRows(normalized, "repositories").map((row) => omitKeys(row, ["description", "stars"]));
  }

  return normalized;
}

function omitKeys(row: Row, keys: string[]): JsonObject {
  return Object.fromEntries(Object.entries(row).filter(([key, value]) => !keys.includes(key) && value !== undefined)) as JsonObject;
}

function sortJson(value: JsonValue): JsonValue {
  if (Array.isArray(value)) {
    return value.map(sortJson);
  }

  if (value && typeof value === "object") {
    return Object.fromEntries(
      Object.entries(value)
        .sort(([a], [b]) => a.localeCompare(b))
        .map(([key, nested]) => [key, sortJson(nested)]),
    );
  }

  return value;
}

