type LeaderboardPoint = {
  label: string;
  passAt1: number;
  cost: number;
};

function renderEfficiencyChart(rows: Row[]): string {
  const points = getLeaderboardPoints(rows);
  if (points.length === 0) {
    return "_No chart data published._";
  }

  const maxCost = Math.max(...points.map((point) => point.cost));

  return [
    "```mermaid",
    "quadrantChart",
    "    title DeepSWE score vs average cost per task",
    "    x-axis Higher avg cost --> Lower avg cost",
    "    y-axis Lower pass at 1 --> Higher pass at 1",
    "    quadrant-1 Most efficient",
    "    quadrant-2 Higher score / higher cost",
    "    quadrant-3 Lower score / higher cost",
    "    quadrant-4 Lower score / lower cost",
    ...points.map((point) => `    ${toMermaidPointLabel(point.label)}: [${formatChartNumber(1 - point.cost / maxCost)}, ${formatChartNumber(point.passAt1)}]`),
    "```",
  ].join("\n");
}

function renderPassAt1Chart(rows: Row[]): string {
  const points = getLeaderboardPoints(rows);
  if (points.length === 0) {
    return "_No chart data published._";
  }

  const maxPercent = Math.max(80, Math.ceil(Math.max(...points.map((point) => point.passAt1 * 100)) / 10) * 10);

  return [
    "```mermaid",
    "xychart-beta",
    "    title \"DeepSWE pass@1 leaderboard\"",
    `    x-axis [${points.map((point) => `\"${escapeMermaidString(point.label)}\"`).join(", ")}]`,
    `    y-axis "pass at 1 (%)" 0 --> ${maxPercent}`,
    `    bar [${points.map((point) => (point.passAt1 * 100).toFixed(1)).join(", ")}]`,
    "```",
  ].join("\n");
}

function getLeaderboardPoints(rows: Row[]): LeaderboardPoint[] {
  return rows.flatMap((row) => {
    const model = typeof row.model === "string" ? row.model : null;
    const effort = typeof row.reasoning_efforts === "string" ? row.reasoning_efforts : null;
    const passAt1 = getNumber(row.pass_at_1);
    const cost = getNumber(row.median_cost_usd);
    if (!model || passAt1 === null || cost === null) {
      return [];
    }

    return [{ label: effort ? `${model} [${effort}]` : model, passAt1, cost }];
  });
}

function getNumber(value: JsonValue | undefined): number | null {
  return typeof value === "number" && Number.isFinite(value) ? value : null;
}

function toMermaidPointLabel(label: string): string {
  return label.replace(/[\[\]:,;]/g, "").replaceAll("]", "");
}

function escapeMermaidString(label: string): string {
  return label.replaceAll("\\", "\\\\").replaceAll("\"", "\\\"");
}

function formatChartNumber(value: number): string {
  return value.toFixed(3).replace(/0+$/, "").replace(/\.$/, "");
}

type Row = Record<string, JsonValue | undefined>;
type Column = [key: string, heading: string, formatter?: (value: JsonValue | undefined) => string];

function getArtifact(artifacts: Map<string, JsonObject>, name: string): JsonObject {
  const artifact = artifacts.get(name);
  if (!artifact) {
    throw new Error(`Missing artifact: ${name}`);
  }
  return artifact;
}

function getRows(artifact: JsonObject, key = "rows"): Row[] {
  const rows = artifact[key];
  return Array.isArray(rows) ? (rows as Row[]) : [];
}

function renderTable(rows: Row[], columns: Column[]): string {
  if (rows.length === 0) {
    return "_No rows published._";
  }

  return [
    `| ${columns.map(([, heading]) => heading).join(" | ")} |`,
    `| ${columns.map(() => "---").join(" | ")} |`,
    ...rows.map((row) => `| ${columns.map(([key, , formatter]) => escapeCell(formatter ? formatter(row[key]) : formatValue(row[key]))).join(" | ")} |`),
  ].join("\n");
}

function renderKeyValueTable(record: JsonObject): string {
  const entries = Object.entries(record);
  if (entries.length === 0) {
    return "_No values published._";
  }

  return [
    "| Metric | Value |",
    "| --- | --- |",
    ...entries.map(([key, value]) => `| ${escapeCell(key)} | ${escapeCell(formatValue(value))} |`),
  ].join("\n");
}

function renderCritiques(critiques: JsonObject): string {
  const headline = critiques.headline;
  const models = Array.isArray(critiques.models) ? (critiques.models as Row[]) : [];
  const headlineRows = headline && typeof headline === "object" && !Array.isArray(headline)
    ? Object.entries(headline).map(([dataset, stats]) => ({ dataset, ...(isRecord(stats) ? stats : {}) }))
    : [];
  const modelRows = models.map((row) => ({
    model: row.model,
    deepswe_bad_rate: getNestedNumber(row.deepswe, "bad_rate"),
    deepswe_fp_rate: getNestedNumber(row.deepswe, "fp_rate"),
    deepswe_fn_rate: getNestedNumber(row.deepswe, "fn_rate"),
    pro_bad_rate: getNestedNumber(row.pro, "bad_rate"),
    pro_fp_rate: getNestedNumber(row.pro, "fp_rate"),
    pro_fn_rate: getNestedNumber(row.pro, "fn_rate"),
  }));

  return [
    renderTable(headlineRows, [
      ["dataset", "Dataset"],
      ["n_reviewed", "Reviewed"],
      ["n_bad", "Bad"],
      ["bad_rate", "Bad rate", formatPercent],
      ["fp_rate", "False-positive rate", formatPercent],
      ["fn_rate", "False-negative rate", formatPercent],
    ]),
    "",
    renderTable(modelRows, [
      ["model", "Model"],
      ["deepswe_bad_rate", "DeepSWE bad rate", formatPercent],
      ["deepswe_fp_rate", "DeepSWE FP", formatPercent],
      ["deepswe_fn_rate", "DeepSWE FN", formatPercent],
      ["pro_bad_rate", "SWE-Bench Pro bad rate", formatPercent],
      ["pro_fp_rate", "SWE-Bench Pro FP", formatPercent],
      ["pro_fn_rate", "SWE-Bench Pro FN", formatPercent],
    ]),
  ].join("\n");
}

function isRecord(value: JsonValue | undefined): value is JsonObject {
  return Boolean(value && typeof value === "object" && !Array.isArray(value));
}

function getNestedNumber(value: JsonValue | undefined, key: string): number | null {
  if (!isRecord(value)) {
    return null;
  }
  const nested = value[key];
  return typeof nested === "number" ? nested : null;
}

function renderCorpusStats(corpusStats: JsonObject): string {
  const rows = ["deep_swe", "swebenchpro", "swebenchverified"].map((name) => {
    const stats = corpusStats[name];
    return {
      dataset: name,
      ...(stats && typeof stats === "object" && !Array.isArray(stats) ? stats : {}),
    };
  });

  return renderTable(rows, [
    ["dataset", "Dataset"],
    ["n_tasks", "Tasks"],
    ["prompt_chars_mean", "Mean prompt chars", formatNumber],
    ["prompt_chars_median", "Median prompt chars", formatNumber],
    ["patch_lines_mean", "Mean patch lines", formatNumber],
    ["patch_lines_median", "Median patch lines", formatNumber],
    ["patch_files_mean", "Mean patch files", formatNumber],
    ["patch_files_median", "Median patch files", formatNumber],
  ]);
}

function formatValue(value: JsonValue | undefined): string {
  if (value === undefined || value === null) {
    return "";
  }
  if (typeof value === "number") {
    return formatNumber(value);
  }
  if (typeof value === "boolean") {
    return value ? "true" : "false";
  }
  if (typeof value === "string") {
    return value;
  }
  return JSON.stringify(value);
}

function formatPercent(value: JsonValue | undefined): string {
  if (typeof value !== "number") {
    return formatValue(value);
  }
  return `${(value * 100).toFixed(1)}%`;
}

function formatUsd(value: JsonValue | undefined): string {
  if (typeof value !== "number") {
    return formatValue(value);
  }
  return `$${value.toFixed(2)}`;
}

function formatSeconds(value: JsonValue | undefined): string {
  if (typeof value !== "number") {
    return formatValue(value);
  }
  return `${formatNumber(value)}s`;
}

function formatNumber(value: JsonValue | undefined): string {
  if (typeof value !== "number") {
    return formatValue(value);
  }
  return Number.isInteger(value) ? value.toLocaleString("en-US") : value.toLocaleString("en-US", { maximumFractionDigits: 2 });
}

function escapeCell(value: string): string {
  return value.replaceAll("|", "\\|").replaceAll("\n", "<br>");
}

await main();
